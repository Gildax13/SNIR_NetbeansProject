    private void JButtonSuiteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonSuiteActionPerformed
        poids = (double) jSpinnerPoids.getValue();
        age = (int) jSpinnerAge.getValue();
        nom = jTextFieldNom.getText();
        prenom = jTextFieldPrenom.getText();
        taille = (double) jSpinnerTaille.getValue();
        jTextAreaAfficheur.append("\nBonjour " + prenom + " " + nom);
        imc = (double) poids / (taille*taille);
        jTextAreaAfficheur.append("\nVotre indice de masse corporel est de: " + imc);
        int indiceCorpulence = 0;
        for (int i = 0; i < NBIMC - 1; i++) {
            if (imc > imcs[i] && imc <= imcs[i + 1]) {
                indiceCorpulence = i + 1;
            }
        }
// cas extreme
        if (imc < 16.5) {
            indiceCorpulence = 0;
        }
        if (imc > 40) {
            indiceCorpulence = NBCORPULENCE - 1;
        }
        jTextAreaAfficheur.append("\nVotre corpulence est qualifiée de " + corpulences[indiceCorpulence]);
        
        if(infoVis == true){
            jPanelPoidsIdeal.setVisible(true);
            jPanelInformations.setVisible(false);
            infoVis = false;
        } else {
            jPanelPoidsIdeal.setVisible(false);
            jPanelInformations.setVisible(true);
            infoVis = true;
        }

    }//GEN-LAST:event_JButtonSuiteActionPerformed

    private void JButtonDevineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonDevineActionPerformed
        double poidsIdeal = 0;
        if (jRadioFemme.isSelected()) {
            poidsIdeal = 45.5 + 2.3 * (taille / 0.0254 - 60.0);
        }
        if (jRadioHomme.isSelected()) {
            poidsIdeal = 50.0 + 2.3 * (taille / 0.0254 - 60.0);
        }
        if (poidsIdeal > 0) {
            jTextAreaAfficheur.append("\nVotre poids ideal avec la formule de devine : " + ((Double) poidsIdeal).toString() + " kg\n");
            double ecart = poids - poidsIdeal;
            if (ecart >= 0) {
                jTextAreaAfficheur.append("\nVous devez perdre " + ((Double) ecart).toString() + " kg\n");
            } else {
                jTextAreaAfficheur.append("\nVous devez prendre " + ((Double) (-ecart)).toString() + " kg\n");
            }
        }
    }//GEN-LAST:event_JButtonDevineActionPerformed

    private void JButtonLorentzActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonLorentzActionPerformed
        double poidsIdeal = 0;
        if (jRadioFemme.isSelected()) {
            poidsIdeal = taille*100-100-(taille*100-150)/2.5;
        }
        if (jRadioHomme.isSelected()) {
            poidsIdeal = taille*100-100-(taille*100-150)/4;
        }
        if (poidsIdeal > 0) {
            jTextAreaAfficheur.append("\nVotre poids ideal avec la formule de lorentz : " + ((Double) poidsIdeal).toString() + " kg\n");
            double ecart = poids - poidsIdeal;
            if (ecart >= 0) {
                jTextAreaAfficheur.append("\nVous devez perdre " + ((Double) ecart).toString() + " kg\n");
            } else {
                jTextAreaAfficheur.append("\nVous devez prendre " + ((Double) (-ecart)).toString() + " kg\n");
            }
        }
    }//GEN-LAST:event_JButtonLorentzActionPerformed

