    private void toucheEtoileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_toucheEtoileMouseClicked
        String text = textMess.getText();
        String test = "1234";
        if (text.equals(test)) {
            textMess.setBackground(Color.green);
            textMess.setText("Code Bon");
            compteurErreur = 0;
        } else {
            textMess.setBackground(Color.red);
            textMess.setText("Code Faux");
            compteurErreur++;
            if (compteurErreur >= 3) {
                textMess.setText("Clavier bloqu√©");
                bloque = true;
                
            }
        }
    }//GEN-LAST:event_toucheEtoileMouseClicked

